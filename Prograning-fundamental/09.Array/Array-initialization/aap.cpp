#include <iostream>
using namespace std;

int main(){
    // Declare Array
    int myarr[5] = {0,1,2};

    // Inserting elements in an array
    myarr[3] = 3;
    myarr[4] = 5;

    // Accessing an Element of an Array in C++
    cout<<myarr[0]<<endl;
    cout<<myarr[1]<<endl;
    cout<<myarr[2]<<endl;
    cout<<myarr[3]<<endl;
    cout<<myarr[4]<<endl;

}